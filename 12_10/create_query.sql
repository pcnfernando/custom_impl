CREATE TABLE WSO2_SP_SCHEDULED_ALERTS
(
  "MSG_ID"     VARCHAR2(128 BYTE) DEFAULT NULL,
  "TPC"      VARCHAR2(128 BYTE) DEFAULT NULL,
  "TEMPLATE_TYPE"      VARCHAR2(128 BYTE) DEFAULT NULL,
  "CUST_ID"      VARCHAR2(128 BYTE) DEFAULT NULL,
  "ALR_TY_CD"    VARCHAR2(128 BYTE) DEFAULT NULL,
  "EACT_CD" VARCHAR2(128 BYTE) DEFAULT NULL,
  "MQ_SRC_NM" VARCHAR2(128 BYTE) DEFAULT NULL,
  "MSG_REC_NM" VARCHAR2(128 BYTE) DEFAULT NULL,
  "CREATED_TIME"   VARCHAR2(1000 BYTE),
  "SHEDULED_TIME" VARCHAR2(1000 BYTE),
