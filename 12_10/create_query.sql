(
  "MSG_ID"     VARCHAR2(128 BYTE) DEFAULT NULL,
  "TPC"      VARCHAR2(128 BYTE) DEFAULT NULL,
  "CRT_TS"      VARCHAR2(128 BYTE) DEFAULT NULL,
  "MQ_SRC_NM"      VARCHAR2(128 BYTE) DEFAULT NULL,
  "ALR_TY_CD" VARCHAR2(128 BYTE) DEFAULT NULL,
  "ACTN_STNG"   VARCHAR2(2000 BYTE),
 CONSTRAINT "PK_OutputTable" PRIMARY KEY ("MSG_ID", "TPC")
